package org.itenas.is.oop.model;

public class Car {
	private String merk;
	private String color;
	private String factory;
	private String doors;
	private String engine;
	private String driver;
	private float speed;
	private double price;
	private int usage;
	
	public Car(String merk, String color, String factory, String doors, String engine, String driver, float speed, double price, int usage) {
		this.merk = merk;
		this.color = color;
		this.factory = factory;
		this.doors = doors;
		this.engine = engine;
		this.driver = driver;
		this.speed = speed;
		this.price = price;
		this.usage = usage;
		
		
	}
}
